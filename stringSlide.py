x_string = "abcdefghijklmn"
print("x_string is" + x_string)

print("x_string[0] = " + x_string[0])

print("x_string[0:] = " + x_string[0:])

print("x_string[0:3] = " + x_string[0:3])

print("x_string[:] = " + x_string[:])

print("x_string[::2] = " + x_string[::2])

# 负数

print("x_string[-1] = " + x_string[-1])

print("x_string[0:-1] = " + x_string[0:-1])

print("x_string[-3:] = " + x_string[-3:])

print("x_string[-3:-1] = " + x_string[-3:-1])

print("x_string[::-1] = " + x_string[::-1])